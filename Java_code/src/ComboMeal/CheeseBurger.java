package ComboMeal;

public class CheeseBurger {
    private int size;
    private double price;

    public CheeseBurger(int size, double price) {
        this.size = size;
        this.price = price;
    }

    public int getSize() {
        return size;
    }


    public double getPrice() {
        return price;
    }

}
